private  void cekilisYap(){
    try(BufferedReader reader = new BufferedReader(new InputStreamReader(new FileInputStream(dosyaYolu),"ISO-8859-9"))){// burada Turkce karekterleri de algilasin diye sanner yerine buffered kullandik 
        String kisi ;
        while ((kisi =reader.readLine()) != null){
            katilanlar.add(kisi);
            
        }
        
}       catch (FileNotFoundException ex) {
            Logger.getLogger(cekilis.class.getName()).log(Level.SEVERE, null, ex);
        } catch (UnsupportedEncodingException ex) {
            Logger.getLogger(cekilis.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(cekilis.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    while(kazananlar.size() != 10 ){
        Random random = new Random();
        int kazananindex = random.nextInt(katilanlar.size());
        kazananlar.add(katilanlar.get(kazananindex));
        
        
    }
}       
    private void cekilis_yapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cekilis_yapActionPerformed
       if (this.dosyaYolu.equals(null)){
           JOptionPane.showMessageDialog(this , "lutfen bir cekilis dosyasi seciniz ");
           
       }
       else{
           cekilisYap();
           for (String kazanan : kazananlar){
               model.addElement(kazanan);
           }
       }
    }//GEN-LAST:event_cekilis_yapActionPerformed

